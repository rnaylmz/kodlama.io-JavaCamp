package week2.classes.reCapDemo;

public class DortIslem {

    public int Topla(int sayi, int sayi1){
        return sayi + sayi1;
    }

    public int Cikar(int sayi, int sayi1){
        return sayi - sayi1;
    }

    public int Carp(int sayi, int sayi1){
        return sayi * sayi1;
    }

    public int Bol(int sayi, int sayi1){
        return sayi / sayi1;
    }
}
